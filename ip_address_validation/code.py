import re

ipv4 = r'^(([0-9])|([1-9][0-9])|(1[0-9][0-9])|(2[0-5][0-5]))\.(([0-9])|([1-9][0-9])|(1[0-9][0-9])|(2[0-5][0-5]))\.(([0-9])|([1-9][0-9])|(1[0-9][0-9])|(2[0-5][0-5]))\.(([0-9])|([1-9][0-9])|(1[0-9][0-9])|(2[0-5][0-5]))$'
ipv6 = r'^([a-f]|[0-9]){1,4}:([a-f]|[0-9]){1,4}:([a-f]|[0-9]){1,4}:([a-f]|[0-9]){1,4}:([a-f]|[0-9]){1,4}:([a-f]|[0-9]){1,4}:([a-f]|[0-9]){1,4}:([a-f]|[0-9]){1,4}$'

size = int(input())

for _ in range(size):
    st = input()
    if re.search(ipv4,st):
        print("IPv4 ")
    elif re.search(ipv6,st):
        print("IPv6")
    else:
        print("Neither")
